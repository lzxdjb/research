"""Local HDL judging utilities used by the HDL RL agent loop.

The judge intentionally uses the isolated toolchain generated by
``hdl_env/rebuild_hdl_env.sh``.  It supports two submission modes:

* SystemVerilog/Verilog: extract RTL and run Slang, Verilator, simulation, and
  Yosys according to task metadata.
* Chisel/Scala: compile/elaborate with Maven/Chisel first, then judge the
  emitted SystemVerilog through the same RTL path.
"""

from __future__ import annotations

import json
import os
import re
import shlex
import shutil
import subprocess
import tempfile
from dataclasses import dataclass
from functools import lru_cache
from pathlib import Path
from typing import Any

_REPO_ROOT = Path(__file__).resolve().parents[2]
_DEFAULT_ENV_SH = _REPO_ROOT / "hdl_env" / "env.sh"
_DEFAULT_WORK_BASE = _REPO_ROOT / "hdl_env" / "tmp" / "agent_judge"

_FENCE_RE = re.compile(r"```([A-Za-z0-9_+.#-]*)\s*\n(.*?)```", re.DOTALL)
_HDL_TAG_RE = re.compile(r"<(?:HDL|RTL|SYSTEMVERILOG|VERILOG|CHISEL|SCALA)>(.*?)</(?:HDL|RTL|SYSTEMVERILOG|VERILOG|CHISEL|SCALA)>", re.DOTALL | re.IGNORECASE)
_TOOL_RESPONSE_RE = re.compile(r"<tool_response>.*?</tool_response>", re.DOTALL | re.IGNORECASE)
_THINK_RE = re.compile(r"<think>.*?</think>", re.DOTALL | re.IGNORECASE)


@dataclass
class CommandResult:
    name: str
    cmd: list[str]
    returncode: int
    stdout: str
    stderr: str
    timed_out: bool = False

    @property
    def ok(self) -> bool:
        return self.returncode == 0 and not self.timed_out

    @property
    def output(self) -> str:
        text = "\n".join(part for part in (self.stdout, self.stderr) if part).strip()
        return text or "<no output>"


def _truncate(text: str, limit: int = 6000) -> str:
    text = text or ""
    if len(text) <= limit:
        return text
    keep = max(0, (limit - 80) // 2)
    return text[:keep] + "\n...(truncated)...\n" + text[-keep:]


def _as_bool(value: Any, default: bool = False) -> bool:
    if value is None:
        return default
    if isinstance(value, bool):
        return value
    if isinstance(value, str):
        return value.strip().lower() in {"1", "true", "yes", "y", "on"}
    return bool(value)


def _json_loads_maybe(value: Any) -> Any:
    if isinstance(value, str):
        text = value.strip()
        if not text:
            return value
        try:
            return json.loads(text)
        except json.JSONDecodeError:
            return value
    return value


def _load_task(ground_truth: Any, extra_info: dict[str, Any] | None = None) -> dict[str, Any]:
    extra_info = extra_info or {}
    task = _json_loads_maybe(ground_truth)
    if not isinstance(task, dict):
        task = _json_loads_maybe(extra_info.get("hdl_task"))
    if not isinstance(task, dict):
        meta = _json_loads_maybe(extra_info.get("meta_json"))
        if isinstance(meta, dict):
            task = _json_loads_maybe(meta.get("hdl_task"))
    if not isinstance(task, dict):
        raise ValueError("HDL reward ground_truth must be a task JSON object")
    return task


@lru_cache(maxsize=16)
def _load_hidden_tests(path: str) -> dict[str, dict[str, Any]]:
    hidden_path = Path(os.path.expanduser(path)).resolve()
    if not hidden_path.is_file():
        return {}

    text = hidden_path.read_text(encoding="utf-8").strip()
    if not text:
        return {}

    by_task_id: dict[str, dict[str, Any]] = {}
    if hidden_path.suffix != ".jsonl":
        try:
            payload = json.loads(text)
        except json.JSONDecodeError:
            payload = None
        if isinstance(payload, dict):
            for key, value in payload.items():
                if isinstance(value, dict):
                    row = dict(value)
                    row.setdefault("task_id", key)
                    by_task_id[str(row["task_id"])] = row
            return by_task_id

    for line in text.splitlines():
        if not line.strip():
            continue
        row = json.loads(line)
        if isinstance(row, dict) and row.get("task_id"):
            by_task_id[str(row["task_id"])] = row
    return by_task_id


def _merge_hidden_reward_task(task: dict[str, Any], extra_info: dict[str, Any] | None) -> dict[str, Any]:
    extra_info = extra_info or {}
    merged = dict(task)

    inline_hidden = _json_loads_maybe(extra_info.get("hdl_hidden_task"))
    if isinstance(inline_hidden, dict):
        merged.update(inline_hidden)

    hidden_tests_path = extra_info.get("hdl_hidden_tests_path")
    task_id = str(merged.get("task_id") or "")
    if hidden_tests_path and task_id:
        hidden_task = _load_hidden_tests(str(hidden_tests_path)).get(task_id)
        if isinstance(hidden_task, dict):
            merged.update(hidden_task)

    return merged


def task_from_tool_parameters(parameters: dict[str, Any], default_task: dict[str, Any] | None = None) -> dict[str, Any]:
    """Build a judge task from model tool parameters and optional dataset task."""

    task = dict(default_task or {})
    task_payload = _json_loads_maybe(parameters.get("task"))
    if isinstance(task_payload, dict):
        task.update(task_payload)

    for key in (
        "language",
        "top_module",
        "top",
        "testbench_cpp",
        "yosys_script",
        "env_sh",
        "timeout",
        "feedback_max_chars",
    ):
        if parameters.get(key) not in (None, ""):
            task[key] = parameters[key]

    for key in ("synthesis", "slang", "verilator_lint", "stop_on_frontend_error"):
        if parameters.get(key) is not None:
            task[key] = _as_bool(parameters[key])

    task.setdefault("language", "systemverilog")
    task.setdefault("top_module", task.get("top") or "Top")
    return task


def _clean_solution_text(solution_str: str) -> str:
    text = _TOOL_RESPONSE_RE.sub("", solution_str or "")
    text = _THINK_RE.sub("", text)
    return text.strip()


def _ensure_trailing_newline(text: str) -> str:
    return text if text.endswith("\n") else text + "\n"


def extract_hdl_code(solution_str: str, language: str | None = None) -> str:
    """Extract candidate HDL/Scala code from a model completion.

    Prefer explicit fenced code blocks and ``<HDL>``-style tags.  If neither is
    present, fall back to a raw module/class region before returning the whole
    cleaned response.
    """

    text = _clean_solution_text(solution_str)
    language = (language or "systemverilog").lower()
    preferred_labels = (
        {"scala", "chisel"}
        if language in {"chisel", "scala"}
        else {"systemverilog", "sv", "verilog", "v", "rtl"}
    )

    fences = [(label.lower(), body.strip()) for label, body in _FENCE_RE.findall(text)]
    for label, body in fences:
        if label in preferred_labels and body:
            return body
    for label, body in fences:
        if body:
            return body

    tag_match = _HDL_TAG_RE.search(text)
    if tag_match:
        return tag_match.group(1).strip()

    if language in {"chisel", "scala"}:
        chisel_start = re.search(r"(?m)^\s*(?:import\s+chisel3|class\s+\w+\s*\(|object\s+\w+\s+extends)", text)
        if chisel_start:
            return text[chisel_start.start() :].strip()
    else:
        modules = list(re.finditer(r"\bmodule\s+\w+\b.*?\bendmodule\b", text, re.DOTALL))
        if modules:
            return "\n\n".join(match.group(0).strip() for match in modules)

    return text


def _env_sh_path(task: dict[str, Any], explicit_env_sh: str | os.PathLike[str] | None = None) -> Path:
    value = explicit_env_sh or task.get("env_sh") or os.environ.get("HDL_ENV_SH") or str(_DEFAULT_ENV_SH)
    return Path(os.path.expanduser(str(value))).resolve()


def _run_cmd(
    name: str,
    args: list[str],
    *,
    cwd: Path,
    env_sh: Path,
    timeout: int,
) -> CommandResult:
    if not env_sh.is_file():
        return CommandResult(
            name=name,
            cmd=args,
            returncode=127,
            stdout="",
            stderr=f"Missing HDL environment file: {env_sh}. Run bash hdl_env/rebuild_hdl_env.sh first.",
        )

    command = "source " + shlex.quote(str(env_sh)) + " && " + " ".join(shlex.quote(str(arg)) for arg in args)
    try:
        proc = subprocess.run(
            ["/bin/bash", "-lc", command],
            cwd=str(cwd),
            text=True,
            stdout=subprocess.PIPE,
            stderr=subprocess.PIPE,
            timeout=timeout,
            env=os.environ.copy(),
        )
        return CommandResult(name=name, cmd=args, returncode=proc.returncode, stdout=proc.stdout, stderr=proc.stderr)
    except subprocess.TimeoutExpired as exc:
        stdout = exc.stdout if isinstance(exc.stdout, str) else (exc.stdout or b"").decode(errors="replace")
        stderr = exc.stderr if isinstance(exc.stderr, str) else (exc.stderr or b"").decode(errors="replace")
        return CommandResult(
            name=name,
            cmd=args,
            returncode=124,
            stdout=stdout,
            stderr=stderr + f"\nTimed out after {timeout}s",
            timed_out=True,
        )


def _write_support_files(task: dict[str, Any], work_dir: Path) -> list[Path]:
    files: list[Path] = []
    for index, item in enumerate(task.get("files") or []):
        if not isinstance(item, dict):
            continue
        rel_path = str(item.get("path") or f"support_{index}.sv")
        safe_path = Path(rel_path)
        if safe_path.is_absolute() or ".." in safe_path.parts:
            raise ValueError(f"Unsafe HDL support file path: {rel_path}")
        path = work_dir / safe_path
        path.parent.mkdir(parents=True, exist_ok=True)
        path.write_text(str(item.get("content") or ""), encoding="utf-8")
        files.append(path)
    return files


